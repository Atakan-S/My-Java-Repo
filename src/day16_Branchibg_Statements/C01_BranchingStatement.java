public class C01_BranchingStatement {
    public static void main(String[] args) {
        //break - continue - return
        for (int i = 0; i < 100; i++) {
            if (i>10){
                break;
            }
            System.out.println(i+"");
        }
        System.out.println("porgram bitti");
    }
}
