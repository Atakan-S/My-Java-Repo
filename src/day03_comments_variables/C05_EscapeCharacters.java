package day03_comments_variables;

public class C05_EscapeCharacters {
public static void main(String[] args){
    // \n -- yeni satıra geçer !!**
    // \t -- bir tab boşluk bırakır!!**
    // \b -- Backspace
    // \" -- " koyar
    // '
    // \r -- koyuldugu yerden itabaren satır başından yazar
    // \\ -- "\" koyar
    System.out.println("one\tTwo\tThree");
    System.out.println("one\bTwo\bThree");
    System.out.println("one\nTwo\nThree\n");
    System.out.println("oneTwo\rThree");



}
}
