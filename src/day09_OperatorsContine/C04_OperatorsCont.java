package day09_OperatorsContine;

public class C04_OperatorsCont {
    public static void main(String[] args) {
        //+,-,++,--
        int num1 = +3;
        int num2 = -5;
        System.out.println(num1>0);
        System.out.println(num2>0);
        System.out.println("-------------------------------");
        int a = 10;
        a++; // a= a+1 // a++ increment denir
        System.out.println("a = " + a);
        a--;// a-- decrement denir
        System.out.println("a = " + a);
        ++a;
        System.out.println("a = " + a);
        System.out.println("++a = " + ++a);//++a preincrement denir
         --a;
        System.out.println("--a = " + --a);
       System.out.println(--a);

        //----------------------------------------------------------

    }
}
